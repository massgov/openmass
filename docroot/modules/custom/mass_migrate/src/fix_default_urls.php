function update_links_in_entity_field($entity, $field_name) {
  $changed = FALSE;
  $field_items = $entity->get($field_name);
  foreach ($field_items as $delta => $item) {
    if (!isset($values[$field_name][$delta])) {
      $values[$field_name][$delta] = $item->getValue();
    }
    switch (get_class($item)) {
      // This is a/the problem.
      case DynamicLinkItem::class:
      case FieldItemList::class:
        // Only update the delta that was migrated
        // (when there are multiple values).
        // Each if() is a different type of DynamicLinkItem
        $item_uri = $item->get('uri')->getString();
        if (strpos($item_uri, 'http://default/') === 0) {
          $uri_new = get_new_uri($item_uri);
          $values[$field_name][$delta]['uri'] = $uri_new;
          $changed = TRUE;
        }
        break;
      default:
    }
  }
  if ($changed) {
    var_dump('saving');
    $entity->$field_name = $values[$field_name];
    $entity->save();
  }

}

function update_paragraph_ref_links($sourceid) {

  $storage = \Drupal::entityTypeManager()->getStorage('paragraph');

  // Query for paragraph items referencing service details node.
  $query = \Drupal::database()->select('entity_usage', 'eu');
  $query->fields('eu', ['source_id', 'source_type']);
  // Filter to paragraph entities that exist.
  $query->innerJoin('paragraphs_item', 'pi', 'eu.source_id=pi.id');
  $query->condition('eu.source_type', 'paragraph');
  $query->condition('eu.target_type', 'node');

  $query->groupBy('eu.source_id');
  $query->groupBy('eu.source_type');
  $query->addField('eu', 'field_name');
  $query->groupBy('eu.field_name');
  $query->condition('eu.target_id', $sourceid);

  $refs = $query->execute()->fetchAll();

  foreach ($refs as $ref) {
    $field_name = $ref->field_name;
    $entity = $storage->load($ref->source_id);
    /** @var \Drupal\Core\Entity\ContentEntityInterface $entity */
    if (!$entity = $storage->load($ref->source_id)) {
      // Not a valid source anymore. Maybe it got deleted.
      var_dump("Unable to load entity");
      continue;
    }
    update_links_in_entity_field($entity, $field_name);
  }
}

function get_new_uri($oldUri) {
  // Since the node no longer exists.
  $nodeAlias = parse_url($oldUri, PHP_URL_PATH);
  if (!$nodeAlias) {
    var_dump('No path found');
    return;
  }
  // Ex: /node/733946.
  $internalPath = \Drupal::service('path_alias.manager')->getPathByAlias($nodeAlias);
  return str_replace('/node/', 'entity:node/', $internalPath);
}

